function normalizeGenreName(value: string) {
  return value
    .trim()
    .toLowerCase()
    .replace(/’/g, "'")
    .replace(/\s+/g, ' ');
}

export function translateGenreName(
  englishName: string,
  language: AppLanguage,
) {
  const key = normalizeGenreName(englishName);
  const item = genreNames[key];

  return item?.[language] || item?.en || englishName;
}